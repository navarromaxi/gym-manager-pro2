const sanitizeGymName = (name: string) => {
  const cleaned = name
    .replace(/^\s*Gimnasio\s+/i, "")
    .replace(/^['"]+|['"]+$/g, "")
    .trim();

  return cleaned.length > 0 ? cleaned : name.trim();
};

